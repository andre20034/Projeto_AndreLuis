    public void preencheTabela(){
        DefaultTableModel model = (DefaultTableModel) TBtabela.getModel();
        model.setNumRows(0);
        ContatoDAO cdao = new ContatoDAO();
        
        for (Contato c: cdao.lerBanco()) {
            model.addRow(new Object[]{
                c.getId(), 
                c.getNome(),
                c.getTelefone(),
                c.getEmail(),
                c.getDataAniversario()
            });
        }
    }
